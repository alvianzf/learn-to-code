/**
 * In order to give let our code be Dynamic, we should add a conditinal response based on the input
 * we use the if function for it to work
 * 
 * the idea is that if a condition is deemed true, or fulfilled, then the commands following it will be excecuted
 * otherwise, the codes will be ignored altogether
 */

const input = 'car'

// we set the condition that, the input must be a car
 if(input == 'car') {
     console.log('it is a car')
 }
 // if not, it will run this block
 else {
     console.log('it is not a car')
 }

